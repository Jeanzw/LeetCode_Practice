                total_value = M[i][j]  #è¿™é‡Œå°±æ˜¯è‡³å°‘ä¼šè®¡ç®—å½“å‰æ ¼å­çš„å€¼ï¼Œå°±æ˜¯åˆ†å­
                if i - 1 >= 0 and j - 1 >= 0:  #æˆ‘ä»¬è¿™é‡Œçœ‹çš„å°±æ˜¯å·¦ä¸Šè§’çš„æ ¼å­
                    total_cell += 1
